#include"../libft.h"

void	*r_matrix_free(char	**s)
{
	int	i;

	i = 0;
	if(s)
	{
		while (s[i])
		{
			r_free(&s[i]);
			i++;
		}
		free(s);
	}
	return (NULL);
}
